package entidades;

public class Contato {
	private String nome;
	private int telefone;

	public String getNome() {
		return nome;
	}
	public int getTelefone() {
		return telefone;
	}
	

	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setTelefone(int telefone) {
		this.telefone = telefone;
	}
	

}
